package modelo;

public class Valor_Cuotas {
	private java.sql.Date fecha_desde;
	private Double valor_cuota;
	
	public java.sql.Date getFecha_desde() {
		return fecha_desde;
	}
	public void setFecha_desde(java.sql.Date fecha_desde) {
		this.fecha_desde = fecha_desde;
	}
	public Double getValor_cuota() {
		return valor_cuota;
	}
	public void setValor_cuota(Double valor_cuota) {
		this.valor_cuota = valor_cuota;
	}
	
}
